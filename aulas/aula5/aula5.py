# Operadores aritméticos

# + - * / // ** %

# * - pode ser usado para repetição (inteiro com string, por exemplo)

# + - concatenar também

# // - divisão inteira (resultado é arredondado)
# ** - exponenciação
# % - mod - resto da divisão

#######################################################################################################

# Multiplicação

print('Multiplicação * ', 10 * 10)


# Divisão

print('Divisão / ', 10 + 10)

# Divisão inteira - arredonda valor para nº inteiro
print(10.5 // 3.5)

# Potênciação
print(2 ** 4)

# Mod (%) - retorna o resto da divisão
print(10 & 5)


###############################################################################

